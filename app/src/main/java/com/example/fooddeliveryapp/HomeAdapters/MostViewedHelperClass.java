package com.example.fooddeliveryapp.HomeAdapters;

public class MostViewedHelperClass {
    int image;
    String text, desc;

    public MostViewedHelperClass(int image, String text) {
        this.image = image;
        this.text = text;
        this.desc = desc;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }

    public String getDesc() {
        return desc;
    }
}
